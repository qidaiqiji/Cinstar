                <div id="chapter1" class="chapter-title">
                    <h3>Chapter 1: What is fine pitch LED display?</h3>
                </div> 
                <p>
                Fine pitch LED display also called narrow pixel pitch LED display or small pixel pitch LED display, normally refers to the LED display which with pixel pitch lower than 3mm. But considering the quick changing of consuming market and LED display industry, fine pitch now refers to the pixel pitch lower than 2mm. At present, the trend is 1.56mm and 1.2mm, but 0.9mm is ready and it goes to lower pixel pitch in the very near future for sure.
                </p> 
                <p>
                The current fine pitch LED display is mainly using 3 in 1 SMD, but more and more leading encapsulation companies are investing big capital on the research of Mini LED and Micro LED to realize even lower pixel pitch. No one can predict where the technology will bring us to.
                </p>  
                <div id="chapter2" class="chapter-title">
                    <h3>Chapter 2: What is 480P, 720P, 1080P, 2K and 4K</h3>
                </div>
                <div class="img-box">
                    <img src="../images/products/Fine-Pitch/fine-pitch-2.jpg"/>
                </div>
                <p>
                As the current most preferable visual displaying system, fine pitch LED display is adopted to display high definition or ultra-high definition content. Referring to the resolution, you should need to figure out what is 480P, 720P, 1080P, 2K and 4K.
480P is the shorthand name for a family of video display resolutions. The 480 means a vertical resolution of 480 pixels. It is usually used for DVD and many early plasma televisions. The Standard definition has always been a 4:3 aspect ratio with a resolution of 640 × 480 pixels.
                </p>
                <p>
                720P is the standard high-definition (HD) display resolution of 1280x720 pixels, at a 16:9 aspect ratio. It is the most common resolution for any major HDTV broadcasting standards.
                </p>
                <p>
                1080P also known as Full HD or FHD. It is with the display resolution of 1920x1080 pixels and could show more details than 720P. This format resolution is usually adopted on a widescreen with aspect ratio of 16:9. 
2K is a quite common resolution that fine pitch LED display frequently required to display. It stands for the resolution of 2048x1080. Even if it has same pixels at vertical and very close pixels at horizontal as 1080P, they are normally defined as two different resolution formats
                </p>
                <p>
                4k also known as ultra-high definition(UHD). In television and consumer media, 4K refers to display resolution of 3840x2160 which is the dominant 4K standard. But it refers to display resolution of 4096x2160 to the movie projection industry. At present, 4K television occupied a big market share, so as to the fine pitch LED display. Nobody refuses a display which capable of showing more detailed and clearer video content. 
                </p>
                <p>
                When you are looking for a fine pitch LED display, it is important to figure out what resolution it should be. Taking 4K resolution for example, the lower pixel pitch, the smaller screen dimension required. And the cost of different pixel pitch varies a lot, after comparison, you will find out what pixel pitch and dimension close to your budget. Besides, the viewing distance and visual experience (image quality on the screen) should be also taken into consideration.
                </p>
