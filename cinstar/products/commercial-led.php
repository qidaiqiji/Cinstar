                <div id="chapter3" class="chapter-title">
                    <h3>Chapter 3: How the LED advertising screen can be controlled</h3>
                </div>
                <div class="img-box">
                    <img src="../images/products/Commercial-LED/commercial-4.jpg"/>
                </div>
                <p>
                According to the manners of signal transmission, the controlling of a outdoor LED screen could be divided into two types: synchronous control and asynchronous control. Here is the difference of them.
                </p>
                <p class="sub-title"><b>Synchronous control</b></p>
                <p>Synchronous control means that the LED advertising screen will display the text, image or video from controlling computer or other input devices like TV signal, camera or VCR etc.</p>
                <p>The real-time signal transmission requires the LED advertising screen is always connected to the controlling computer or video processor. If connection fails, the LED advertising display will lose the signal and display nothing.</p>
                <p>The synchronous control is widely used for commercial LED advertising display, rental LED display, sports LED display and other big format LED displays.</p>
                <p class="sub-title"><b>Asynchronous control</b></p>
                <p>Asynchronous controlling LED advertising screen can work independently and don’t have to always remain connected to controlling computer. The computer is only needed when you are going to edit the advertising content.</p>
                <p>All the advertisement content will be sent to the control card inside the LED advertising screen via a UTP cat5 cable, USB disk, a WiFi connection, 4G wireless or others form of data transmissions.</p>
                <p>When the data is transmitted successfully to the control card, the data will be saved in its memory card. Then you can cut off the connection. The control card will display the videos and images until you turn it off from your side.</p>
                <p>Asynchronous control is ideal for the installations that the controlling computer is far from LED advertising screen or the installation location is hard to do the wiring. This controlling method is usually being used for small size LED display screen, roadside LED display or outdoor advertising LED display.</p>
